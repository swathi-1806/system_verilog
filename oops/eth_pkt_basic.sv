class eth_pkt;
  rand bit [55:0]preamble;
  rand bit [7:0]sof;
  rand bit [47:0]sa;
  rand bit [47:0]da;
  rand bit [15:0]len;
  	   bit [31:0]crc;
  rand byte payload[$];
  
  function void print();
    $display("---------------------------------------------------------------------------------------------------------------------");
    $display("preamble=%0d",preamble);
    $display("sof=%0d",sof);
    $display("sa=%0d",sa);
    $display("da=%0d",da);
    $display("len=%0d",len);
    $display("crc=%0d",crc);
    $display("payload=%p",payload);
  endfunction
  
  constraint a_c{
    len inside {[10:25]};
    payload.size()==len;
  }
  
endclass

module tb;
  eth_pkt pkt[10];
  	initial begin
      for(int i=0;i<10;i++)begin
        pkt[i]=new();
        pkt[i].randomize();
        pkt[i].print();
      end
    end
endmodule

